package Day_0526;

public class CastString_06 {
    public static void main(String[] args) {

        String num1 = "20";
        String num2 = "30";
//      문자를 숫자로 형변환
//      Integer.parseInt
        int num = Integer.parseInt(num1);

        System.out.println(num1 + num2);
        System.out.println(num + 20);
    }
}
