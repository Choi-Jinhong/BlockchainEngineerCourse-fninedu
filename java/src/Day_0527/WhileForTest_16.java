package Day_0527;

public class WhileForTest_16 {
    public static void main(String[] args) {
        int n = 10;
        while (n >= 0){
            System.out.println(n--+" ");
        }
        System.out.println();

        for(int a = 10; a >= 0; a--){
            System.out.println(a + " ");
        }
        System.out.println();
    }
}